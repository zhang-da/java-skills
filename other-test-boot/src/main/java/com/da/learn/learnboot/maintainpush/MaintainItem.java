package com.da.learn.learnboot.maintainpush;

public class MaintainItem {
    private Double firstMileage;
    private Double regularMileage;

    private Double lastMileage;

    public Double getFirstMileage() {
        return firstMileage;
    }

    public void setFirstMileage(Double firstMileage) {
        this.firstMileage = firstMileage;
    }

    public Double getRegularMileage() {
        return regularMileage;
    }

    public void setRegularMileage(Double regularMileage) {
        this.regularMileage = regularMileage;
    }

    public Double getLastMileage() {
        return lastMileage;
    }

    public void setLastMileage(Double lastMileage) {
        this.lastMileage = lastMileage;
    }
}
